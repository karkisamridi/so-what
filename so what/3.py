# For taking principal form user
P = float(input("Enter Principal of simple interest rate:"))
# For taking time from user
T = float(input("Enter Time in years:"))
# For taking Intrest Rate from user
R = float(input("Enter interest rate in percent:"))
# To find simple interest rate
simple_interest_rate = P*T*R/100
# To print simple interest rate
print(simple_interest_rate)